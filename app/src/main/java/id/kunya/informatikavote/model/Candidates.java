package id.kunya.informatikavote.model;

public class Candidates {
    private String idkandidat;
    private String tahun;
    private String visi;
    private String misi;
    private String fotoprofilketua;
    private String fotoprofilwakil;
    private String nim_calonketua;
    private String nim_calonwakil;
    private String nama_ketua;
    private String nama_wakil;

    public String getIdkandidat() {
        return idkandidat;
    }

    public void setIdkandidat(String idkandidat) {
        this.idkandidat = idkandidat;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public String getVisi() {
        return visi;
    }

    public void setVisi(String visi) {
        this.visi = visi;
    }

    public String getMisi() {
        return misi;
    }

    public void setMisi(String misi) {
        this.misi = misi;
    }

    public String getFotoprofilketua() {
        return fotoprofilketua;
    }

    public void setFotoprofilketua(String fotoprofilketua) {
        this.fotoprofilketua = fotoprofilketua;
    }

    public String getFotoprofilwakil() {
        return fotoprofilwakil;
    }

    public void setFotoprofilwakil(String fotoprofilwakil) {
        this.fotoprofilwakil = fotoprofilwakil;
    }

    public String getNim_calonketua() {
        return nim_calonketua;
    }

    public void setNim_calonketua(String nim_calonketua) {
        this.nim_calonketua = nim_calonketua;
    }

    public String getNim_calonwakil() {
        return nim_calonwakil;
    }

    public void setNim_calonwakil(String nim_calonwakil) {
        this.nim_calonwakil = nim_calonwakil;
    }

    public String getNama_ketua(){
        return nama_ketua;
    }

    public void setNama_ketua(String nama_ketua) {
        this.nama_ketua = nama_ketua;
    }

    public String getNama_wakil(){
        return nama_wakil;
    }

    public void setNama_wakil(String nama_wakil) {
        this.nama_wakil = nama_wakil;
    }
}
